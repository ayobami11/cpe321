#include <iostream>
// contains square root function (sqrt)
#include <cmath>

using namespace std;

int main(void) {

    cout << "\nThe square root of 25 is: " << sqrt(25);

    cout << "\nThe square root of 11.25 is: " << sqrt(11.25);

    cout << "\nThe square root of 500 is: " << sqrt(500);

    cout << endl;

    system("pause");

    return 0;
}